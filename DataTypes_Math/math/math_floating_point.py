import math

# arithmetic

num1 = 2.67

ceil = math.ceil(num1) # The smallest integer greater than or equal to x
floor = math.floor(num1) # The largest integer less than or equal to x
trunc = math.trunc(num1) # The integer part of x

print(f"The ceiling of {num1} is {ceil}; the floor is {floor}; the truncation is {trunc}")
