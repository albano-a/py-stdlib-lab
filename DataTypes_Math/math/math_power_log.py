import math

print(
    math.exp(2),          # e ** x
    math.expm1(1e-5),     # e**x - 1
    math.log(10),         # ln(10)
    math.log(100, 10),    # log base 10
    math.log1p(1e-5),     # log(1 + x)
    math.log2(8),         # log base 2
    math.log10(1000),     # log base 10
    math.pow(2, 3),       # power, always float
    math.sqrt(9),         # square root
)
